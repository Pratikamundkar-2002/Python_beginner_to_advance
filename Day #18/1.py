# _____ WHILE LOOP ______________________
a=0
while (a<4):     # used for stopping the loop till 3
    print(a)     # first prints a=0 goes down
    a = 1+a      #adds 1 no. in a ie. 0+1=1
# and continue the ITERATION till a<4 ie. a=3
# after that comes out of the loop.
print("done with the loop")

