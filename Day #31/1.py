
# SETS in Python

# SETS ARE UNORDERED COLLECTION OF DATAITEMS

# We can store different dataitems in sets
# set pishvi sarkha ahe , jyat apan datatypes takhu shakto

sets= {1,23,1,6,1,78}
print(sets)    # a value if stored more than one time gets printed only once
print(type(sets))  # 1 will be printed only once

set={1, True, "hello", 1.90, "a"}  # in set, it does not print the values in serial manner
print(set)

pratik = set()       # this is a empty set
print(type(pratik))

for value in set:    #accessing values thru for loop
    print(value)


